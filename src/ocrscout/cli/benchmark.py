"""`ocrscout benchmark` — run a matrix of models (× GPU types × runners).

Phase 2 ships a single-runner local-only matrix: the current GPU vs. N
models. The Parquet output already carries ``gpu_type`` / ``provider`` /
``cost_per_hour`` columns (stamped per-row at write time from
``~/.ocrscout/config.yaml`` or env vars), so the same output is
self-describing across cells when Phase 3's ``SkyPilotRunner`` arrives
and a single ``ocrscout benchmark`` invocation can fan out across L4 /
RTX5000 / GB10 / … pods.

Pages are sampled deterministically once (via ``--seed``) and every
model sees the exact same pages, so cross-model differences are
attributable to the models rather than to sampling noise. The output
directory carries a per-run id (``benchmark/<run_id>/``) under the
user-supplied ``--output`` root; the run id is generated by ocrscout if
not provided so subsequent ``ocrscout costs --output ...`` invocations
can group by model and produce the summary table.
"""

from __future__ import annotations

import logging
import os
import uuid
from pathlib import Path

import typer
from rich import print as rprint
from rich.table import Table

from ocrscout import state as state_mod
from ocrscout.cli import app
from ocrscout.errors import RunnerError, ScoutError
from ocrscout.exports.layout import parquet_dest
from ocrscout.log import setup_logging
from ocrscout.registry import registry
from ocrscout.types import AdapterRef, PipelineConfig

log = logging.getLogger(__name__)


@app.command("benchmark")
def benchmark(
    models: str = typer.Option(
        ..., "--models", "-m",
        help="Comma-separated profile names to benchmark.",
    ),
    pages: int = typer.Option(
        ..., "--pages",
        help="Total pages to sample. Same pages are used across every model.",
    ),
    source: str | None = typer.Option(
        None, "--source", "-s",
        help="Image source: a local directory, fsspec URL, or HF Hub dataset id.",
    ),
    source_name: str = typer.Option(
        "hf_dataset", "--source-name",
        help="Source adapter name.",
    ),
    seed: int = typer.Option(42, "--seed"),
    output: Path = typer.Option(
        Path("./ocrscout-benchmark"), "--output", "-o",
        help="Output root; the run lands under <output>/<run-id>/.",
    ),
    run_id: str | None = typer.Option(
        None, "--run-id",
        help="Run identifier (subdirectory name). Defaults to a UUID prefix.",
    ),
    runner_names: str = typer.Option(
        "local", "--runners",
        help="Comma-separated runner names. Phase 2 only supports 'local'; "
             "Phase 3+ adds 'skypilot' / 'hf'.",
    ),
    gpu_types: str | None = typer.Option(
        None, "--gpu-types",
        help="Comma-separated GPU labels for stamping (L4, RTX5000, …). The "
             "local runner ignores values beyond stamping; SkyPilot uses "
             "them to provision the right pod.",
    ),
    base_port: int = typer.Option(8000, "--base-port"),
    proxy_port: int = typer.Option(4000, "--proxy-port"),
    gpu_budget: float = typer.Option(0.85, "--gpu-budget"),
    verbose: int = typer.Option(0, "-v", "--verbose", count=True),
    quiet: bool = typer.Option(False, "-q", "--quiet"),
) -> None:
    """Run a matrix of models against the same sampled pages."""
    setup_logging(verbosity=verbose, quiet=quiet)

    model_list = [m.strip() for m in models.split(",") if m.strip()]
    runner_list = [r.strip() for r in runner_names.split(",") if r.strip()]
    gpu_list = (
        [g.strip() for g in gpu_types.split(",") if g.strip()]
        if gpu_types
        else [None]
    )

    if len(runner_list) > 1 or runner_list != ["local"]:
        # SkyPilot / HF arrive in Phase 3/4. For now refuse silently
        # rather than launching a partial matrix.
        unsupported = [r for r in runner_list if r != "local"]
        if unsupported:
            raise typer.BadParameter(
                f"--runners {unsupported!r} not yet implemented; "
                "Phase 2 supports 'local' only (Phase 3 adds 'skypilot', "
                "Phase 4 adds 'hf')."
            )

    run_id = run_id or uuid.uuid4().hex[:8]
    run_dir = output / run_id
    run_dir.mkdir(parents=True, exist_ok=True)
    log.info("Benchmark run dir: %s", run_dir)

    # Phase 2 collapses the GPU matrix to a single cell (whatever's
    # locally available). The ``gpu_types`` list still parametrises the
    # output-column stamp so the user gets a labelled run.
    gpu_label = gpu_list[0]
    if gpu_label is not None:
        os.environ["OCRSCOUT_GPU_TYPE"] = gpu_label

    source_args: dict[str, object] = {"path": source, "sample": pages, "seed": seed}
    cfg = PipelineConfig(
        name=f"benchmark-{run_id}",
        source=AdapterRef(name=source_name, args=source_args),
        models=model_list,
        export=AdapterRef(name="parquet", args={"dest": str(parquet_dest(run_dir))}),
        sample=pages,
        output_dir=run_dir,
    )

    try:
        runner_cls = registry.get("runners", "local")
    except ScoutError as e:
        raise typer.BadParameter(str(e)) from e
    runner = runner_cls()

    # Always launch fresh (benchmark = controlled matrix, not reuse)
    existing = state_mod.read_state()
    if existing is not None:
        log.info("benchmark: existing runner detected; tearing down for a clean matrix")
        runner.down()

    try:
        handle = runner.launch(
            models=model_list,
            gpu_type=gpu_label,
            base_port=base_port,
            proxy_port=proxy_port,
            gpu_budget=gpu_budget,
        )
        os.environ["OCRSCOUT_VLLM_URL"] = handle.proxy_url
    except RunnerError as e:
        log.error("Benchmark launch failed: %s", e)
        raise typer.Exit(code=1) from e

    try:
        from ocrscout.pipeline.engine import PipelineEngine

        PipelineEngine().execute_on_proxy(cfg, proxy_url=handle.proxy_url)
    finally:
        try:
            runner.down()
        except Exception as e:  # noqa: BLE001
            log.warning("Runner teardown reported error: %s", e)

    _summarise(run_dir, model_list, gpu_label)


def _summarise(run_dir: Path, models: list[str], gpu_label: str | None) -> None:
    """Print a per-model summary from the run's Parquet shards via DuckDB."""
    try:
        import duckdb
    except ImportError:
        rprint(
            f"[yellow]Output written to {run_dir}; install duckdb for the "
            f"summary table.[/yellow]"
        )
        return

    glob = run_dir / "data" / "train-*.parquet"
    try:
        df = duckdb.sql(
            f"""
            SELECT
                model,
                count(*) AS pages,
                sum(coalesce(elapsed_seconds, 0)) AS gpu_seconds,
                avg(elapsed_seconds) AS avg_s_per_page,
                sum(coalesce(litellm_cost, 0)) AS token_cost,
                sum(coalesce(gpu_time_cost, 0)) AS infra_cost
            FROM read_parquet('{glob}')
            GROUP BY model
            ORDER BY model
            """
        ).df()
    except Exception as e:  # noqa: BLE001
        rprint(f"[yellow]Summary query failed: {e}[/yellow]")
        return

    title = f"benchmark summary  (gpu={gpu_label or '—'}, run={run_dir.name})"
    table = Table(title=title)
    for col in df.columns:
        table.add_column(col)
    for row in df.itertuples(index=False):
        table.add_row(
            *(_fmt(cell) for cell in row)
        )
    rprint(table)


def _fmt(value: object) -> str:
    if value is None:
        return "—"
    if isinstance(value, float):
        return f"{value:.4f}" if abs(value) < 1 else f"{value:,.2f}"
    return str(value)
